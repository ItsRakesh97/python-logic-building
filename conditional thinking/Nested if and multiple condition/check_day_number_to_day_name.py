# Take a day number (1–7) and print the corresponding day name.

day = int(input("\nenter day number (1 to 7): "))

if  day == 1:
    print("monday")

elif day == 2:
    print("tuesday")

elif day == 3:
    print("wednesday")

elif day == 4:
    print("thursday")

elif day == 5:
    print("friday")

elif day == 6:
    print("saturday")

else:
    print("sunday")
